function ask2_function(A)
ph = phase(A); %Υπολογισμός της φάσης με χρήση της συνάρτησης phase
ab = abs(A);   %Υπολογισμός του μέτρου με χρήση της συνάρτησης abs
re = real(A);  %Υπολογισμός του πραγματικού μέρους με την χρήση της real
im = imag(A);  %Υπολογισμός του φανταστικού μέρους με την χρήση της imag
disp(ph);
disp(ab);     
               %Εκτύπωση αποτελεσμάτων 
disp(re);
disp(im);
end

